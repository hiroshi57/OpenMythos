    @staticmethod
    def load_reports(json_paths: List[str]) -> List[BenchmarkReport]:
        """
        JSON ファイルのリストから BenchmarkReport を読み込む。

        Parameters
        ----------
        json_paths : list[str]
            JSON ファイルパスのリスト (時系列順)

        Returns
        -------
        list[BenchmarkReport]
            タイムスタンプ昇順にソートされた BenchmarkReport のリスト
        """
        reports: List[BenchmarkReport] = []
        for p in json_paths:
            try:
                reports.append(GrowingAIBenchmark.load(p))
            except Exception:
                continue
        return sorted(reports, key=lambda rp: rp.timestamp)

    @staticmethod
    def trend_table(
        json_paths: List[str],
        n: Optional[int] = None,
    ) -> str:
        """
        過去 N 回分の JSON を読み込んでパターン別 KPI 改善率トレンド表を生成する。

        各行: 実行日時 / 成功率 / 平均改善率 / パターン別 Δ%

        Parameters
        ----------
        json_paths : list[str]
            JSON ファイルパスのリスト
        n : int, optional
            最新 N 件のみ使用する (省略時は全件)

        Returns
        -------
        str
            Markdown 形式のトレンド表文字列。
            JSON ファイルが 0 件の場合は空文字列を返す。
        """
        reports = ReportGenerator.load_reports(json_paths)
        if not reports:
            return ""

        if n is not None:
            reports = reports[-n:]

        # パターン ID の全集合を収集 (全レポート横断)
        all_pids: List[str] = []
        seen: set[str] = set()
        for rp in reports:
            for res in rp.results:
                if res.pattern_id not in seen:
                    all_pids.append(res.pattern_id)
                    seen.add(res.pattern_id)

        # ヘッダー行
        pid_headers = "".join(f" | {pid.upper()} Δ%" for pid in all_pids)
        header = f"| 実行日時 | 成功率 | 平均Δ%{pid_headers} |"
        separator = "|" + "---|" * (4 + len(all_pids))

        # データ行
        data_rows: list[str] = []
        for rp in reports:
            pid_map = {res.pattern_id: res.improvement_pct for res in rp.results}
            pid_cells = "".join(
                f" | {pid_map[pid]:+.1f}%" if pid in pid_map else " | —"
                for pid in all_pids
            )
            data_rows.append(
                f"| {rp.timestamp[:16]} "
                f"| {rp.success_rate * 100:.0f}% "
                f"| {rp.avg_improvement_pct:+.1f}%"
                f"{pid_cells} |"
            )

        lines = [
            "## KPI 改善率トレンド",
            "",
            header,
            separator,
        ] + data_rows + [""]

        return "\n".join(lines)


