def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="ベンチマーク結果レポートジェネレーター")
    parser.add_argument(
        "--input", "-i", type=str, default=None,
        metavar="JSON",
        help="入力 JSON ファイル (BenchmarkReport)",
    )
    parser.add_argument(
        "--markdown", "-m", type=str, default=None,
        metavar="MD",
        help="Markdown 出力先ファイルパス",
    )
    parser.add_argument(
        "--html", type=str, default=None,
        metavar="HTML",
        help="HTML 出力先ファイルパス",
    )
    parser.add_argument(
        "--dashboard", "-d", type=str, default=None,
        metavar="HTML",
        help="インタラクティブ ダッシュボード HTML の出力先ファイルパス (Chart.js 使用)",
    )
    parser.add_argument(
        "--trend", nargs="+", default=None,
        metavar="JSON",
        help="トレンド表生成: 複数 JSON ファイルを指定",
    )
    parser.add_argument(
        "--trend-n", type=int, default=None,
        metavar="N",
        help="トレンド表: 最新 N 件のみ使用",
    )
    return parser.parse_args()


def main() -> None:
    args = _parse_args()

    # トレンド表モード
    if args.trend:
        table = ReportGenerator.trend_table(args.trend, n=args.trend_n)
        if table:
            print(table)
        else:
            print("(ベンチマーク結果が見つかりませんでした)")
        return

    # 単一レポートモード
    if not args.input:
        print("--input または --trend を指定してください", file=sys.stderr)
        sys.exit(1)

    report = GrowingAIBenchmark.load(args.input)
    gen = ReportGenerator(report)

    if args.markdown:
        p = gen.save_markdown(args.markdown)
        print(f"Markdown saved: {p}")

    if args.html:
        p = gen.save_html(args.html)
        print(f"HTML saved: {p}")

    if args.dashboard:
        # --trend が同時指定されている場合はトレンドデータも埋め込む
        trend_rps = None
        if args.trend:
            trend_rps = ReportGenerator.load_reports(args.trend)
        p = gen.save_dashboard(args.dashboard, trend_reports=trend_rps)
        print(f"Dashboard saved: {p}")

    if not args.markdown and not args.html and not args.dashboard:
        # デフォルト: Markdown を stdout に出力
        print(gen.to_markdown())


if __name__ == "__main__":
    main()
